
#array of integer
arr=[1,2,3,4,5]

#traversing element of array
print("printing array elements ")
for item in arr:
    print(item,end=" ")


#traversing array of elements in reverse order

print(" ")
print("printing array elements in reverse order")
for i in range(len(arr)-1,-1,-1):
    print(arr[i],end=" ")










